/**
 * Formats a file using Biome.
 * @param filePath The path to the file to format.
 */
async function format(filePath: string): Promise<void> {
  console.log('ЁЯОи Formatting file with Biome...')
  try {
    await execAsync(`pnpm biome format --write ${filePath}`)
    console.log('тЬЕ Biome formatting complete.')
  } catch (e: any) {
    console.error('тЭМ Biome formatting failed:', e.stdout || e.stderr)
    throw new Error('Biome formatting failed.')
  }
}

/**
 * Checks a file with TypeScript compiler.
 * @param filePath The path to the file to check.
 */
async function checkTypeScript(filePath: string): Promise<void> {
  console.log('ЁЯзР Checking file with TypeScript compiler...')
  try {
    await execAsync(`pnpm tsc --noEmit --skipLibCheck ${filePath}`)
    console.log('тЬЕ TypeScript check passed.')
  } catch (e: any) {
    console.error('тЭМ TypeScript check failed:', e.stdout || e.stderr)
    throw new Error('TypeScript check failed.')
  }
}

/**
 * Main function to update the languages.ts file.
 */
async function updateLanguagesFile(): Promise<void> {
  console.log('ЁЯЪА Starting to update languages.ts...')
  try {
    const extractedLanguages = extractAllLanguageData()
    const fileContent = generateLanguagesFileContent(extractedLanguages)

    await fs.writeFile(LANGUAGES_FILE_PATH, fileContent, 'utf-8')
    console.log(`тЬЕ Successfully wrote to ${LANGUAGES_FILE_PATH}`)

    await format(LANGUAGES_FILE_PATH)
    await checkTypeScript(LANGUAGES_FILE_PATH)

    console.log('ЁЯОЙ Successfully updated languages.ts file!')
    console.log(`ЁЯУК Contains ${Object.keys(extractedLanguages).length} languages.`)
  } catch (error) {
    console.error('тЭМ An error occurred during the update process:', (error as Error).message)
    // No need to restore backup as we write only at the end of successful generation.
    process.exit(1)
  }
}

if (require.main === module) {
  void updateLanguagesFile()
}

export { updateLanguagesFile }
